    public void cargarComboEmp() {
        cboEmp.removeAllItems();

        List<Empleado> empleados = eD.listarEmpleados();

        cboEmp.addItem(null);

        for (Empleado empleado : empleados) {
            cboEmp.addItem(empleado);
        }

    }

