    public void cargarInformePedidosMesaHora() {
        double total = 0;
        if (dcFechaMesa.getDate() == null) {
            return;
        }
        Mesa m = (Mesa) cboMesas.getSelectedItem();
        LocalDate fecha = dcFechaMesa.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        DateTimeFormatter formato = DateTimeFormatter.ofPattern("HH:mm");
        LocalTime horaDesde = LocalTime.parse(cboHoraDesde.getSelectedItem() + ":" + cboMinutosDesde.getSelectedItem(), formato);
        LocalTime horaHasta = LocalTime.parse(cboHoraHasta.getSelectedItem() + ":" + cboMinutosHasta.getSelectedItem(), formato);
        List<Pedido> detalles;
        if (cboMesas.getSelectedItem() != null) {
            detalles = peD.listarPedidosPorMesaPorHora(m, fecha, horaDesde, horaHasta);
        } else {
            detalles = peD.listarPedidosPorHora(fecha, horaDesde, horaHasta);
        }
        //List<DetallePedido> detalles = dpD.listarDetallePedidosPorEmpleadoPreciosHora(e.getIdEmpleado());
        modeloMesas.setRowCount(0);
        String pago = "";
        for (Pedido p : detalles) {
            if (p.isPago()) {
                pago = "Pago";
                total += p.getImporte();
            } else {
                if (p.getEstado() == 0) {
                    pago = "Cancelado";
                } else {
                    pago = "Sin pagar";
                }
            }
            DateTimeFormatter f = DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm:ss");
            String horario = p.getFechaHora().format(f);
            modeloMesas.addRow(new Object[]{p.getIdPedido(), p.getMesa().getIdMesa(),
                p.getEmpleado().getApellido() + ", " + p.getEmpleado().getNombre(), horario, pago, p.getImporte()});

        }
        lblTotal1.setText(total + "");
    }

