    public void cargarInformePedidos() {
        double total = 0;
        if (dcFecha.getDate() == null) {
            return;
        }
        Empleado e = (Empleado) cboMozos.getSelectedItem();
        LocalDate fecha = dcFecha.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        List<Pedido> detalles;
        if (cboMozos.getSelectedItem() != null) {
            detalles = peD.listarPedidosPorMesero(e, fecha);
        } else {
            detalles = peD.listarPedidosPorFecha(fecha);
        }
        //List<DetallePedido> detalles = dpD.listarDetallePedidosPorEmpleadoPreciosHora(e.getIdEmpleado());
        modelo.setRowCount(0);
        String pago = "";
        for (Pedido p : detalles) {
            if (p.isPago()) {
                pago = "Pago";
                total += p.getImporte();
            } else {
                if (p.getEstado() == 0) {
                    pago = "Cancelado";
                } else {
                    pago = "Sin pagar";
                }

            }
            modelo.addRow(new Object[]{p.getIdPedido(), p.getMesa().getIdMesa(), p.getEmpleado().getNombre() + " " + p.getEmpleado().getApellido(), pago, p.getImporte()});

        }
        lblTotal.setText(total + "");
    }

